/* ---------- Локальний стан демо (localStorage) ---------- */
const LS_KEY = "postup-demo-state-v1";
function loadState() {
  try { return JSON.parse(localStorage.getItem(LS_KEY)) || {}; }
  catch { return {}; }
}
function saveState(st) { localStorage.setItem(LS_KEY, JSON.stringify(st)); }
function resetState() { localStorage.removeItem(LS_KEY); location.reload(); }

/* Застосувати локальні донати з демо поверх базових даних */
(function applyLocalDonations() {
  const st = loadState();
  if (!st.extraRaised) return;
  for (const s of DB.students) {
    const extra = st.extraRaised[s.id];
    if (extra) { s.raised = Math.min(s.goal, s.raised + extra.sum); s.supporters += extra.count; }
  }
})();
